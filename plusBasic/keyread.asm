;-----------------------------------------------------------------------------
; Clear alternate keyboard port FIFO buffer
; Cobbered: A
;-----------------------------------------------------------------------------
key_clear_fifo:
    xor       a
    out       (IO_KEYBUFF),a
    ret
    
;-----------------------------------------------------------------------------
; Read ASCII key from alternate keyboard port
; Output: A: Key ASCII value (0 = no key pressed)
;-----------------------------------------------------------------------------
key_read_ascii:
    in        a,(IO_KEYBUFF)
    ret
    
